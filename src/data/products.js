const createCustomizableFields = ({
  textLabel,
  textPlaceholder,
  textMaxLength = 30,
  textRequired = false,
  optionOneLabel,
  optionOneValues,
  optionOneDefaultValue,
  optionTwoLabel,
  optionTwoValues,
  optionTwoDefaultValue,
}) => ({
  textInput: {
    type: "text",
    name: "customText",
    label: textLabel,
    placeholder: textPlaceholder,
    maxLength: textMaxLength,
    required: textRequired,
  },
  options: [
    {
      type: "select",
      name: "optionOne",
      label: optionOneLabel,
      values: [...optionOneValues],
      defaultValue: optionOneDefaultValue ?? optionOneValues[0] ?? "",
    },
    {
      type: "select",
      name: "optionTwo",
      label: optionTwoLabel,
      values: [...optionTwoValues],
      defaultValue: optionTwoDefaultValue ?? optionTwoValues[0] ?? "",
    },
  ],
});

